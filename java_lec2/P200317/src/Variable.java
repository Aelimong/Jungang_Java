
class Variable {

	public static void main(String[] args) {
		// 정수타입
		// byte a=100;
		// short b=100;
		int c = 100;
		// long d=100;

		System.out.println(c);

		// 실수타입
		// float e=3.3333f;
		double f = 3.3333;

		// 문자타입(기본타입 중 유일한 문자형)
		// char g = '안';
		// 보라색은 문법적인 키워드
		String h = "안녕하세요"; // 참조변수 call by reference
		// 기본타입이 아니다, 클래스형
		
		//boolean , 1byte생성
		boolean i = true;
		i = false; //false에 해당하는 어떤 코드값 들어감

	}

}
